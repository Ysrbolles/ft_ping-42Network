#include "libft.h"

char	*ft_strdup(const char *src)
{
	char	*new;
	int		i;
	int		l;

	l = ft_strlen(src);
	new = (char*)malloc(sizeof(*src) * (l + 1));
	if (new == NULL)
		return (NULL);
	i = 0;
	while (src[i] != '\0')
	{
		new[i] = src[i];
		i++;
	}
	new[i] = '\0';
	return (new);
}
