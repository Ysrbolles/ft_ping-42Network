#include "libft.h"

char	*ft_strstr(const char *s1, const char *s2)
{
	size_t	j;

	if (*s2 != '\0')
	{
		j = ft_strlen(s2);
		while (ft_strncmp(s1, s2, j) != 0)
		{
			if (*s1 == '\0')
				return (0);
			s1++;
		}
	}
	return ((char*)s1);
}
