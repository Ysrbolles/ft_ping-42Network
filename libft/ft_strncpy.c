#include "libft.h"

char	*ft_strncpy(char *s1, const char *s2, size_t n)
{
	size_t	i;
	int		k;

	i = 0;
	k = 0;
	while (i < n)
	{
		if (s2[i] && !k)
			s1[i] = s2[i];
		else
		{
			s1[i] = '\0';
			k = 1;
		}
		i++;
	}
	return (s1);
}
